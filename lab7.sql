// zad.1A

CREATE TABLE A6_LRS(
    GEOM SDO_GEOMETRY
);

// zad.1B

INSERT INTO A6_LRS
SELECT SR.GEOM 
FROM STREETS_AND_RAILROADS SR, MAJOR_CITIES MC
WHERE SDO_RELATE(SR.GEOM, 
    SDO_GEOM.SDO_BUFFER(MC.GEOM, 10, 1, 'unit=km'), 'MASK=ANYINTERACT') = 'TRUE' AND
    MC.CITY_NAME = 'Koszalin';
    
// zad.1C

select SDO_GEOM.SDO_LENGTH(GEOM, 1, 'unit=km') DISTANCE,
ST_LINESTRING(GEOM).ST_NUMPOINTS() ST_NUMPOINTS
from A6_LRS;

// zad.1D

UPDATE A6_LRS
SET GEOM = SDO_LRS.CONVERT_TO_LRS_GEOM(GEOM, 0, 276.681);

